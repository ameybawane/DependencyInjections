/*
 * What is Dependency Injection ?
 * Dependency Injection (DI) is a software pattern.
 * DI is basically providing the objects that an object needs, 
 * instead of having it construct the objects themselves.
 * DI is a technique whereby one object supplies the dependencies of another object.
 * With the help of DI, we can write loosely coupled code.
 * DI is achieved by writing loosely couple code.
 * A loosely-coupled code is a code where all your classes can work independently 
 * without relying on each other.
 * 
 * Types of Dependency Injection
 * Constructor Injection
 * Setter or property Injection
 * Method Injection
 * 
 * DIS-ADVANTAGES OF TIGHT COUPLING
 * Tight coupling is when a group of classes are highly dependent on one another.
 * This scenario arises when a class assumes too many responsibilities, or when 
 * one concern is spread over many classes rather than having its own class.
 * Difficult to maintain
 * Difficult to test
 * 
 * ADVANTAGES OF LOOSE COUPLING
 * Loose coupling means that the classes are independent of each other.
 * Loose coupling is achieved by means of a design that promotes single-responsibility 
 * and separation of concerns.
 * Easy to maintain
 * Easy to test
 */
